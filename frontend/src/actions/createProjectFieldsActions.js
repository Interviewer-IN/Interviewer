export function fillProjectTitleFields(title) {
    return {
        type: "FILL_TITLE",
        payload: title
    };
}

export function fillProjectDescriptionFields(description) {
    return {
        type: "FILL_DESCRIPTION",
        payload: description
    };
}

export function clearProjectTitleFields(title) {
    return {
        type: "FILL_TITLE",
        payload: title
    };
}

export function clearProjectDescriptionFields(description) {
    return {
        type: "FILL_DESCRIPTION",
        payload: description
    };
}